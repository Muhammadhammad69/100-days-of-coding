// use compound assignment operators.

function compoundAssignmentOperator(){
    let x = 4; //declare the variable.

    //Addition with compound Assignment operators.
    x += 4;

    //Now Substraction.
    x -= 3;

    //Multiplication.
    x *= 8;

    //Division.
    x /= 2;

}


compoundAssignmentOperator();
//this function perform the compound assignment operators.