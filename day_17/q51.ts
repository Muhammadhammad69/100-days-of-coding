// using Original methods.
function calAreaOfRentangle(width: number, height: number) {
    console.log(`Area of Rentangle = ${width * height}`);
};

// using arrow function.

let calAreaOfRentangleWithArrow = (width: number, height: number) => {
    // Print the Area of Rentangle.
    console.log(`Area of Rentangle = ${width * height}`);
};

// call the arrow function.
calAreaOfRentangleWithArrow(8, 69);